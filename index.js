// Aithmetic Operators
let x = 10;
let y = 3;

console.log(x + y);  // (x+y) This is an exprassion
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);
console.log(x ** y); // This is x power of y

// Increment (++)
console.log(++x);
console.log(x++);

// Decrement (--)
console.log(--x);
console.log(x--);